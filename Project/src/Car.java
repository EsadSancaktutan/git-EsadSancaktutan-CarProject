
public class Car {
    private int id;
    private String car_brand;
    private String car_sherry;
    private String car_model;
    private String car_year;        
    private String car_fuel;        
    private String car_gear;        
    private String car_km;        
    private String car_caseType;        
    private String car_motorPower;        
    private String car_color;        
    private String car_price;

    public Car(int id, String car_brand, String car_sherry, String car_model, String car_year, String car_fuel, String car_gear, String car_km, String car_caseType, String car_motorPower, String car_color, String car_price) {
        this.id = id;
        this.car_brand = car_brand;
        this.car_sherry = car_sherry;
        this.car_model = car_model;
        this.car_year = car_year;
        this.car_fuel = car_fuel;
        this.car_gear = car_gear;
        this.car_km = car_km;
        this.car_caseType = car_caseType;
        this.car_motorPower = car_motorPower;
        this.car_color = car_color;
        this.car_price = car_price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCar_brand() {
        return car_brand;
    }

    public void setCar_brand(String car_brand) {
        this.car_brand = car_brand;
    }

    public String getCar_sherry() {
        return car_sherry;
    }

    public void setCar_sherry(String car_sherry) {
        this.car_sherry = car_sherry;
    }

    public String getCar_model() {
        return car_model;
    }

    public void setCar_model(String car_model) {
        this.car_model = car_model;
    }

    public String getCar_year() {
        return car_year;
    }

    public void setCar_year(String car_year) {
        this.car_year = car_year;
    }

    public String getCar_fuel() {
        return car_fuel;
    }

    public void setCar_fuel(String car_fuel) {
        this.car_fuel = car_fuel;
    }

    public String getCar_gear() {
        return car_gear;
    }

    public void setCar_gear(String car_gear) {
        this.car_gear = car_gear;
    }

    public String getCar_km() {
        return car_km;
    }

    public void setCar_km(String car_km) {
        this.car_km = car_km;
    }

    public String getCar_caseType() {
        return car_caseType;
    }

    public void setCar_caseType(String car_caseType) {
        this.car_caseType = car_caseType;
    }

    public String getCar_motorPower() {
        return car_motorPower;
    }

    public void setCar_motorPower(String car_motorPower) {
        this.car_motorPower = car_motorPower;
    }

    public String getCar_color() {
        return car_color;
    }

    public void setCar_color(String car_color) {
        this.car_color = car_color;
    }

    public String getCar_price() {
        return car_price;
    }

    public void setCar_price(String car_price) {
        this.car_price = car_price;
    }
    
}
