    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String brand = brand_field.getText();
        String sherry = sherry_field.getText();
        String modelll = model_field.getText();
        String year = year_field.getText();
        String fuel = fuel_field.getText();
        String gear = gear_field.getText();
        String km = km_field.getText();
        String casetype = casetype_field.getText();
        String motorpower = motorpower_field.getText();
        String color = color_field.getText();
        String price = price_field.getText();

        int selectedrow = car_tabel.getSelectedRow();

        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                    message3.setText("Car table null!");
            }
            else{
                message3.setText("Car selected update!");
            }
        }
        
        else{
            int id = (int)model.getValueAt(selectedrow,0);
            op.carUpdate(id,brand, sherry, modelll, year, fuel, gear, km, casetype, motorpower, color, price);
            CarView();
            message1.setText("Car update succesfull");
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

