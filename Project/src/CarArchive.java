    public void filter(String query){
        
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<DefaultTableModel>(model);
        car_tabel.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(query));
        
    }
    
    
        public void CarView(){
            model.setRowCount(0);
            ArrayList<Car> cars = new ArrayList<Car>();
            cars = op.CarCome();
            
            if(cars != null){
                for(Car carx : cars){
                   Object[] add = {
                      carx.getId(),carx.getCar_brand(),carx.getCar_sherry(),carx.getCar_model(),carx.getCar_year(),carx.getCar_fuel(),carx.getCar_gear(),carx.getCar_km(),carx.getCar_caseType(),carx.getCar_motorPower(),carx.getCar_color(),carx.getCar_price()
                   };
             model.addRow(add);
             }
        }
    }
    
